        this.Empusername = Empusername;
        this.Emppassword = Emppassword;
        this.EmpbankaccNO = EmpbankaccNO;
        this.EmpbankName = EmpbankName;
        this.EmpuserType = EmpuserType;
        this.Empaddress = Empaddress;
